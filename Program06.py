# Adding strings
Greeting ="Good Morning, "

name = "Sahil"
print(Greeting+name)

# String Slicing
print(name[3]) #Printing character at index 3

print(name[0:3]) #Prints Sah.    here first index is included and last index is not. 
print(name[:4]) #same as name[0:4
print(name[-1]) #Prints character at last index
